function [eta, f] = voigtApprox(fl, fg)
    f = (1.00000*fg.*fg.*fg.*fg.*fg + ...
        2.69269*fg.*fg.*fg.*fg.*fl + ...
        2.42843*fg.*fg.*fg.*fl.*fl + ...
        4.47163*fg.*fg.*fl.*fl.*fl + ...
        0.07842*fg.*fl.*fl.*fl.*fl + ...
        1.00000*fl.*fl.*fl.*fl.*fl)^(1/5);

    eta = 1.36603*(fl./f) - 0.47719*(fl.*fl./f./f) + 0.11116*(fl.*fl.*fl./f./f./f);
end